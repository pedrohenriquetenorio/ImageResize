    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        try {
               BufferedImage iconimage = new BufferedImage(icon.getIconWidth(), icon.getIconHeight(),BufferedImage.TYPE_INT_ARGB);
               Graphics2D g2 = iconimage.createGraphics();
               icon.paintIcon(null, g2, 0, 0);
               g2.dispose();
               ImageIO.write(iconimage, tipo, F);
               
        } catch (IOException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
               
           
        
    }//GEN-LAST:event_jButtonSalvarActionPerformed
    private void scale(int largura, int altura){
     Icon icon = new ImageIcon(new ImageIcon(F.getAbsolutePath()).getImage().getScaledInstance(altura, largura, Image.SCALE_DEFAULT));
     this.icon = icon;
     jLabelPreview.setIcon(icon);
    }
    
