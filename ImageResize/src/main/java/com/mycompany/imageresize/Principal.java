    private String extension(String namePath){
        String reverse = "";
        for(int b=namePath.length()-1; b>=0;b--){
            reverse += String.valueOf(namePath.charAt(b));
            if(reverse.contains(".")){
                return reverse;
            }
        }
       return "";
    }
    private String converter(String names){
        String reverse = "";
        String name = names.replace(".", "");
        for(int b=name.length()-1; b>=0;b--){
            reverse += String.valueOf(name.charAt(b));
        }
       return reverse;
    }
    
